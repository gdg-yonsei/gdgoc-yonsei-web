export type AdminMessageKey = keyof (typeof adminMessages)['en']
export type AdminMessages = Record<AdminMessageKey, string>

export function getAdminMessages(locale: Locale): AdminMessages {
  return adminMessages[locale] as AdminMessages
}

export function localizeAdminHref(href: string, locale: Locale): string {
  if (!href.startsWith('/admin')) {
    return href
  }
  return `/${locale}${href}`
}

export function getAdminRootPath(locale: Locale): string {
  return `/${locale}/admin`
}

export function getAdminIntlLocale(locale: Locale): string {
  return locale === 'ko' ? 'ko-KR' : 'en-US'
}

export function formatAdminDate(
  value: string | Date,
  locale: Locale,
  options: Intl.DateTimeFormatOptions
): string {
  return new Intl.DateTimeFormat(getAdminIntlLocale(locale), options).format(
    typeof value === 'string' ? new Date(value) : value
  )
}
